package com.xiugou.x1.battle.mapdata2;

/**
 * @author YY
 *
 */
public class MonsterPoint {
	private int id;
	private String name;
	private float x;
	private float y;
	private int max_num;
	private int fog_area;
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public float getX() {
		return x;
	}
	public float getY() {
		return y;
	}
	public int getMax_num() {
		return max_num;
	}
	public int getFog_area() {
		return fog_area;
	}
}
